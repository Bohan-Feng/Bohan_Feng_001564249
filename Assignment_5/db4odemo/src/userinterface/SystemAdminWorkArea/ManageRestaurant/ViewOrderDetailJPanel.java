    void populateCombo(){
        labelHeader.setText("Order " + order.getID() + " for Restaurant " + system.getRestaurantDirectory().getRestaurantByID(order.getRestaurnatID()));
        
        // receiver
        this.comboReceiver.removeAllItems();
        this.comboReceiver.addItem("-1 not set");
        for(Customer c : system.getCustomerDirectory()){
            comboReceiver.addItem(c.getID() + " " + c);
        }
        
        this.comboSender.removeAllItems();
        this.comboSender.addItem("-1 not set");
        for(DeliveryMan dm : system.getDeliveryManDirectory()){
            comboSender.addItem(dm.getID() + " " + dm);
        }
        
        this.comboStatus.removeAllItems();
        comboStatus.addItem("Cooking in progress");
        comboStatus.addItem("ready to pick up");
        comboStatus.addItem("Delivering");
        comboStatus.addItem("Complete");
    }
    
    void populateOrderInfo(){
        // receiver
        for(int i = 0; i < comboReceiver.getItemCount(); i++){
            if(Integer.parseInt(comboReceiver.getItemAt(i).split(" ")[0])==order.getReceiverID()){
                comboReceiver.setSelectedIndex(i);
                break;
            }
        }
        // Sender
        for(int i = 0; i < comboSender.getItemCount(); i++){
            if(Integer.parseInt(comboSender.getItemAt(i).split(" ")[0])==order.getSenderID()){
                comboSender.setSelectedIndex(i);
                break;
            }
        }
        // status
        for(int i = 0; i < comboStatus.getItemCount(); i++){
            if(comboStatus.getItemAt(i).equals(order.getStatus())){
                comboStatus.setSelectedIndex(i);
                break;
            }
        }
        
        txtAddress.setText(order.getAddress());
        txtDetail.setText(order.getOrderInfo());
    }


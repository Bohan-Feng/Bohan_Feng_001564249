    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(inputValiadate()){
            
            order.setOrderInfo(txtDetail.getText());
            order.setAddress(txtAddress.getText());
            order.setReceiverID(Integer
                .parseInt(comboReceiver
                    .getSelectedItem()
                    .toString()
                    .split(" ")[0]));
            order.setSenderID(Integer
                .parseInt(comboSender
                    .getSelectedItem()
                    .toString()
                    .split(" ")[0]));
            order.setStatus(comboStatus.getSelectedItem().toString());
            system.getOrderDirectory().update(order);
            
            javax.swing.JOptionPane.showMessageDialog(null, "Order " + order + "updated");
            goBack();
            
        }
    }//GEN-LAST:event_btnSaveActionPerformed

        private boolean inputValiadate(){
        if(txtDetail.getText().equals("")
                || txtAddress.getText().equals("")){
            javax.swing.JOptionPane.showMessageDialog(null, "text field cannot be empty");
            return false;
        }
        return true;
    }
    
    private void goBack(){
        container.remove(this);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.previous(container);
    }

